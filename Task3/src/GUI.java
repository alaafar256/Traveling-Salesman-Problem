    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        int[][] graph = new int[size][size];
        DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
        int i=0;
        for (int count = 1; count < tblModel.getRowCount(); count++) {
            for (int j = 0; j < tblModel.getColumnCount(); j++) {
                graph[i][j] = Integer.parseInt((tblModel.getValueAt(count, j).toString()));
            }
            i++;
        }
        boolean[] visited = new boolean[size];

        visited[0] = true;
        int minimum = (int) 1e7;
        minimum = minimumweightHamiltonian(graph, visited, 0, size, 1, 0, minimum);

        JOptionPane.showMessageDialog(this, "Minimum Hamiliton's distance is : " + minimum);
        
    }//GEN-LAST:event_jButton2ActionPerformed

