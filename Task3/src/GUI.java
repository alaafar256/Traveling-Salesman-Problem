    static int minimumweightHamiltonian(int[][] graph, boolean[] visited, int CurrentPositionofNode, int NumberofVertices,
            int count, int weight, int minimum) {
        System.out.println("CurrentPosition : " + CurrentPositionofNode);
        System.out.println("N : " + NumberofVertices);
        System.out.println("count : " + count);
        System.out.println("Cost : " + weight);
        System.out.println("ans : " + minimum);
        // Base case , if start node is finish node .
        if (count == NumberofVertices) {
            minimum = Math.min(minimum, weight + graph[CurrentPositionofNode][0]);
            System.out.println("Minimum Weight: " + minimum);
            return minimum;
        }
        for (int i = 0; i < NumberofVertices; i++) {
            if (visited[i] == false) {
                if (graph[CurrentPositionofNode][i] > 0) {
                    System.out.println("graph[CurrentPositionofNode][i]: " + graph[CurrentPositionofNode][i]);

                    visited[i] = true;
                    minimum = minimumweightHamiltonian(graph, visited, i, NumberofVertices, count + 1,
                            weight + graph[CurrentPositionofNode][i], minimum);
                    visited[i] = false;
                }
            }
        }
        return minimum;
    }

